	<h2>Wibroe, Duckert og Partners fusionerer med DialoguePeople, SocialPeople og NetPeople for at skabe fremtidens bureau</h2>

	<div class="info">
		<div class="published">12.12.2012</div>
	</div>

	<div class="article">
		<p>DialoguePeople, NetPeople &amp; SocialPeople fusionerer med Wibroe, Duckert og Partners med sidstnævnte som det fortsættende mærke. Fusionen er et naturligt næste skridt for at skabe fremtidens bureau, hvor kunderne stiller endnu større krav til kreativitet, indhold, forretningstilgang og forståelse for teknologi.</p>
		<p>Der er tale om en fusion mellem de seneste 30 års mest succesfulde kreative reklamebureau og en række specialistbureauer med digitalt og teknologisk afsæt, der alene i 2012 har sat sine aftryk med bl.a. Cannes Lions nominering, som vinder af Creative Circle, DIA, Lovie Award m.v.</p>
